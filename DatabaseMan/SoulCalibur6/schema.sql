DROP TABLE IF EXISTS Characters;
CREATE TABLE Characters (
	ID	INTEGER NOT NULL UNIQUE,
	Name	TEXT NOT NULL UNIQUE,
	Icon	TEXT,
	PRIMARY KEY(ID)
);



